impl TryFrom<u8> for ObjectType {
    type Error = TryFromObjectTypeError;
    fn try_from(value: u8) -> std::result::Result<Self, Self::Error> {
        match value {
            0 => Ok(Self::OBJECT),
            1 => Ok(Self::ITEM),
            2 => Ok(Self::CONTAINER),
            3 => Ok(Self::UNIT),
            4 => Ok(Self::PLAYER),
            5 => Ok(Self::GAME_OBJECT),
            6 => Ok(Self::DYNAMIC_OBJECT),
            7 => Ok(Self::CORPSE),
            _ => Err(TryFromObjectTypeError::new(value))
        }
    }
}

#[derive(Debug)]
pub struct TryFromObjectTypeError {
    value: u8,
}

impl TryFromObjectTypeError {
    pub const fn new(value: u8) -> Self {
        Self { value }
    }
}

#[derive(Debug)]
pub enum ObjectTypeError {
    Read(std::io::Error),
    TryFrom(TryFromObjectTypeError),
}

impl std::error::Error for ObjectTypeError {}
impl std::fmt::Display for TryFromObjectTypeError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.write_fmt(format_args!("invalid value for enum 'ObjectType': '{}'", self.value))
    }
}

impl std::fmt::Display for ObjectTypeError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Read(e) => e.fmt(f),
            Self::TryFrom(e) => e.fmt(f),
        }
    }
}

impl From<std::io::Error> for ObjectTypeError {
    fn from(value: std::io::Error) -> Self {
        Self::Read(value)
    }
}

impl From<TryFromObjectTypeError> for ObjectTypeError {
    fn from(value: TryFromObjectTypeError) -> Self {
        Self::TryFrom(value)
    }
}

